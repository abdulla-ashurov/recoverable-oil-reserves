     subroutine ch1toi1(a,i)
     character*1 a
     integer*2 i
     if(a.eq.' ') i=0
     if(a.eq.'0') i=0
     if(a.eq.'1') i=1
     if(a.eq.'2') i=2
     if(a.eq.'3') i=3
     if(a.eq.'4') i=4
     if(a.eq.'5') i=5
     if(a.eq.'6') i=6
     if(a.eq.'7') i=7
     if(a.eq.'8') i=8
     if(a.eq.'9') i=9
     return
     end
     