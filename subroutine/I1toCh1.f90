       subroutine i1toch1(n,a1)
       character*1 a1
       integer*2 n
       
       if(n.eq.0) a1='0' 
       if(n.eq.1) a1='1'
       if(n.eq.2) a1='2'
       if(n.eq.3) a1='3'
       if(n.eq.4) a1='4'
       if(n.eq.5) a1='5'
       if(n.eq.6) a1='6'
       if(n.eq.7) a1='7'
       if(n.eq.8) a1='8'
       if(n.eq.9) a1='9'
       
       return
       end
